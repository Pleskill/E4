La formation<br/>

	Ce diplôme est un diplôme BAC +2 reconnu par, il apporte des compétences pluridisciplinaires dans le domaine des compétences pluridisciplinaires dans le domaine de la maintenance informatique, à trouver des solutions techniques d'infrastructures, et à développer des applications logicielles.

	Cette formation permet de travailler soit en indépendant, soit en tant que salarié dans plusieurs types d'organisation: entreprises de services numériques, PME / PMI, Société de Services d'Ingénierie Informatique (SSII), services publics, autres<br/><br/>
        

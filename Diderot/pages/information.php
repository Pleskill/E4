		Bac pro gestion-administration<br/>
		Bac pro Systèmes Électroniques Numériques (SEN)<br/>
		Baccalauréat Économique et Sociale (Spécialité Mathématiques)<br/>
		Baccalauréat Littéraire option Mathématiques et connaissances en informatique<br/><br/>

